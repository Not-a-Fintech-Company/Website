// One-off script: generate metrics-aligned fallback @font-face declarations
// for EB Garamond + Hanken Grotesk so CLS from font-display: swap is
// minimized when fonts are loading.
//
// Run with: node scripts/generate-font-fallbacks.mjs
// Then paste the output into src/styles/global.css.

import { createFontStack } from '@capsizecss/core';
import eBGaramond from '@capsizecss/metrics/eBGaramond';
import hankenGrotesk from '@capsizecss/metrics/hankenGrotesk';
import georgia from '@capsizecss/metrics/georgia';
import timesNewRoman from '@capsizecss/metrics/timesNewRoman';
import arial from '@capsizecss/metrics/arial';
import helveticaNeue from '@capsizecss/metrics/helveticaNeue';

// We renamed the family to 'EB Garamond Variable' / 'Hanken Grotesk Variable'
// in our @font-face — override the metrics object's familyName so generated
// fallback names match.
const ebVariable = { ...eBGaramond, familyName: 'EB Garamond Variable' };
const hankenVariable = { ...hankenGrotesk, familyName: 'Hanken Grotesk Variable' };

const serifStack = createFontStack([ebVariable, georgia, timesNewRoman]);
const sansStack = createFontStack([hankenVariable, arial, helveticaNeue]);

console.log('/* ============================================================');
console.log(' * Generated by scripts/generate-font-fallbacks.mjs');
console.log(' * Serif stack (display): EB Garamond Variable -> Georgia -> Times');
console.log(' * ============================================================ */');
console.log(serifStack.fontFaces);
console.log('');
console.log('/* font-family value: */');
console.log('/*', serifStack.fontFamily, '*/');
console.log('');
console.log('/* ============================================================');
console.log(' * Sans stack (UI/body): Hanken Grotesk Variable -> Arial -> Helvetica Neue');
console.log(' * ============================================================ */');
console.log(sansStack.fontFaces);
console.log('');
console.log('/* font-family value: */');
console.log('/*', sansStack.fontFamily, '*/');
